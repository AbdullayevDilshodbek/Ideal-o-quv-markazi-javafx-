package sample.teacher.davomad.lineChart;

public class TeacherChartModul {
    private String fish;
    private String oy;
    private int foiz;

    public TeacherChartModul(String fish, String oy, int foiz) {
        this.fish = fish;
        this.oy = oy;
        this.foiz = foiz;
    }

    public String getFish() {
        return fish;
    }

    public String getOy() {
        return oy;
    }

    public int getFoiz() {
        return foiz;
    }
}
